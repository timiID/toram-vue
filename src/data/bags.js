  export function getEmptySlots(maxSlot = 100) {
    const usedSlots = bags.map(b => b.slot)
    const empty = []
  
    for (let i = 1; i <= maxSlot; i++) {
      if (!usedSlots.includes(i)) {
        empty.push(i)
      }
    }
  
    return empty
  }
  export function filterTypeMob() { 
    const allowed = ['Boss', 'Miniboss', 'Mob', 'Spina'] 
    return bags.filter(b => allowed.includes(b.kelas_mob))
  }  
  
  export function searchBag(keyword) {
    const key = keyword.toLowerCase()
  
    return bags.filter(b =>
      (b.nama_item && b.nama_item.toLowerCase().includes(key)) ||
      (b.monster && b.monster.toLowerCase().includes(key))
    )
  }

  export function paginateBags(page = 1, perPage = 10) {
    const start = (page - 1) * perPage
    const end = start + perPage
  
    return {
      data: bags.slice(start, end),
      total: bags.length,
      page,
      perPage,
      totalPage: Math.ceil(bags.length / perPage),
    }
  }
  export function sortBySlot(asc = true) {
    return [...bags].sort((a, b) =>
      asc ? a.slot - b.slot : b.slot - a.slot
    )
  }
  
  export function sortBySpina(desc = true) {
    return [...bags].sort((a, b) =>
      desc ? b.spina - a.spina : a.spina - b.spina
    )
  }
    
  
      